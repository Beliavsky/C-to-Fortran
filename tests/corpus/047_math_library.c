#include <math.h>
#include <stdio.h>

int main(void) {
    double x = 0.5;

    printf("sqrt = %.8f\n", sqrt(2.0));
    printf("exp = %.8f\n", exp(x));
    printf("log = %.8f\n", log(2.0));
    printf("sin = %.8f\n", sin(x));
    printf("cos = %.8f\n", cos(x));
    printf("pow = %.8f\n", pow(2.0, 3.0));

    return 0;
}
